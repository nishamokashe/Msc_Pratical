
a = 12  # In binary: 1100
b = 5   # In binary: 0101

# Check the Bitwise AND (&) conditions 

result = a & b  
print(f"{a} & {b} = {result}")

# Check the Bitwise OR (|) conditions 

result = a | b  
print(f"{a} | {b} = {result}")

# Check the Bitwise XOR (^) conditions 

result = a ^ b  
print(f"{a} ^ {b} = {result}")

# Check the Bitwise NOT (~) conditions 

result = ~a  
print(f"~{a} = {result}")

# Check the Bitwise Left Shift (<<)conditions 

result = a << 2  
print(f"{a} << 2 = {result}")

# Check the Bitwise Right Shift (>>)conditions
 
result = a >> 2  # Result: 3 (In binary: 0011)
print(f"{a} >> 2 = {result}")

